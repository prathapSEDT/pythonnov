'''
in the list when we have elements scattered here and there, if we want to get the list elements in a
proper order, say like in asc order or in desc order

use the method called as sort()
This method will not return anew list
This method will alter or modify the existing list
'''

mylist=[1,78,43,67,24,9,5,2,7]
mylist.sort(reverse=True)
print(mylist)

alpha=['a','t','c','e','d','m']
alpha.sort()
print(alpha)
